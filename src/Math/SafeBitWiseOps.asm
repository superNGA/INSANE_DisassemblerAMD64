section .text
    global SafeBitWiseOps_And
    global SafeBitWiseOps_Or


;//////////////////////////////////////////////////////////////////////////
;//////////////////////////////////////////////////////////////////////////
SafeBitWiseOps_And:
    mov rax, rcx
    and rax, rdx
    ret


;//////////////////////////////////////////////////////////////////////////
;//////////////////////////////////////////////////////////////////////////
SafeBitWiseOps_Or:
    mov rax, rcx
    or  rax, rdx
    ret
