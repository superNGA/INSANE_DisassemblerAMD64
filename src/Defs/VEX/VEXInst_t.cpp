#include "../../../Include/VEX/VEXInst_t.h"


using namespace InsaneDASM64;


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
VEX::VEXInst_t::VEXInst_t()
{
    Clear();
}


///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////
void VEX::VEXInst_t::Clear()
{
    m_prefix        = 0x00;
    m_nVEXBytes     = -1;
    m_opcode.Clear();
    m_bHasSIB       = false;
    m_disp.Clear();
    m_immediate.Clear();
}
